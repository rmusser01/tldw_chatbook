# tldw_chatbook/tldw_api/__init__.py
"""
tldw_api package: typed HTTP client + ~1,300 Pydantic request/response
schemas for the remote tldw_server REST API.

Lazy re-exports (PEP 562). The names in ``__all__`` below resolve on first
attribute access rather than at package-import time, via module
``__getattr__`` -- the same pattern used by
``Local_Ingestion/__init__.py`` (see that module's docstring for the
general rationale). This matters here specifically because this package
used to eagerly import 54 schema submodules (1271+ names) at
*package* import time -- roughly a third of ``import tldw_chatbook.app``'s
cold-start cost -- even for sessions that never talk to a remote
tldw_server. Regular callers (e.g. ``from tldw_chatbook.tldw_api import
NoteResponse``) are unaffected: the first access still returns the exact
same class object, just resolved on demand instead of at package-init
time, and the result is cached on the module so later access is a plain
attribute lookup. Direct submodule imports (e.g. ``from
tldw_chatbook.tldw_api.feedback_schemas import ExplicitFeedbackRequest``,
used by several ``*_Interop`` packages) still run this ``__init__.py``
first per standard Python import semantics, but that is now cheap since
this module does no eager submodule imports of its own.

Caveat (measured, see task-285 Implementation Notes): ``TLDWAPIClient``
(defined in ``client.py``, a 15k-line module) itself eagerly imports the
same schema surface at *its own* module scope, because its method
signatures reference nearly every request/response type. ~44
``Server*Service`` modules import ``TLDWAPIClient`` from this package at
their own module level, and several of those are themselves imported at
``app.py`` module level. So resolving the ``TLDWAPIClient`` name still
pays close to the full former cost -- this package being lazy mainly
defers/avoids the cost for processes that never touch ``TLDWAPIClient``
(tests, tools, subprocesses that only need a handful of schema classes,
or a future world where ``Server*Service`` construction is itself made
lazy -- tracked separately, out of scope here).
"""

